package org.anshen.test;

import org.junit.Test;

public class FloatToStringTests {
	
	@Test
	public void testFloatToString() {
		System.out.println(.23);
		System.out.println(Integer.valueOf("             12"));
		System.out.println(Float.valueOf("    0.11"));
		System.out.println(Float.valueOf(".23"));
		System.out.println(Float.valueOf("   .232"));
	}

}
